package org.liprudent.majiang.engine.event.impl;

import org.apache.commons.lang.builder.EqualsBuilder;
import org.apache.commons.lang.builder.HashCodeBuilder;
import org.apache.commons.lang.builder.ToStringBuilder;
import org.liprudent.majiang.engine.event.IEvent;
import org.liprudent.majiang.engine.player.IPlayer.Wind;

public abstract class BasicEvent implements IEvent {
	private final String name;
	private final Wind wind;

	public BasicEvent(final String name, final Wind wind) {
		super();
		this.name = name;
		this.wind = wind;
	}

	@Override
	public String getName() {
		return name;
	}

	@Override
	public Wind getPlayerWind() {
		return wind;
	}

	@Override
	public int hashCode() {
		return HashCodeBuilder.reflectionHashCode(this);
	}

	@Override
	public boolean equals(final Object obj) {
		return EqualsBuilder.reflectionEquals(this, obj);
	}

	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this);
	}
}
